"""
A calulator module
"""

def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    return a / b

# build a function to calculate the power of a number
def power(a, b):
    return( a ** b)

# build a function to calculate the square root of a number
def sqrt(a):
    return(a ** 0.5)